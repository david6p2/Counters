import UIKit

/// ` UIColor` extension
extension UIColor {
    static let accentColorDisable = UIColor.accentColor.withAlphaComponent(0.5)
    static let accentColor = UIColor(named: "AccentColor")!
    static let background = UIColor(named: "Background")!
    static let buttonText = UIColor(named: "ButtonText")!
    static let cellBackground = UIColor(named: "CellBackground")!
    static let descriptionText = UIColor(named: "DescriptionText")!
    static let disableText = UIColor(named: "DisabledText")!
    static let countersGreen = UIColor(named: "Green")!
    static let primaryText = UIColor(named: "PrimaryText")!
    static let countersRed = UIColor(named: "Red")!
    static let secondaryText = UIColor(named: "SecondaryText")!
    static let subtitleText = UIColor(named: "SubtitleText")!
    static let countersYellow = UIColor(named: "Yellow")!
}
